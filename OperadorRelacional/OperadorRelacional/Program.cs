var (valorA, valorB, valorC) = (10, 1, 2);

Console.WriteLine("Los valores son: A " + valorA + " B " + valorB + " C " + valorC);

// == igual a
// != diferente a
// mayor que
// menor que
// >=  mayor o igual
// <= menor o igual

bool resultado1 = valorA == valorB;

Console.WriteLine("El resultado de la comparación valorA == valorB: " + resultado1);

bool resultado2 = valorA != valorB;

Console.WriteLine("El resultado de la comparación valorA != valorB: " + resultado2);

bool resultado3= valorA > valorB;

Console.WriteLine("El resultado de la comparación valorA > valorB: " + resultado3);

bool resultado4 = valorA < valorB;

Console.WriteLine("El resultado de la comparación valorA < valorB: " + resultado4);

bool resultado5 = valorA <= valorB;

Console.WriteLine("El resultado de la comparación valorA <= valorB: " + resultado5);

bool resultado7 = valorA >= valorB;

Console.WriteLine("El resultado de la comparación valorA >= valorB: " + resultado7);